n, m = map(int, input().split())
l = []
tak = [
    ['#', '#', '#', '.', '', '', '', '', ''],
    ['#', '#', '#', '.', '', '', '', '', ''],
    ['#', '#', '#', '.', '', '', '', '', ''],
    ['.', '.', '.', '.', '', '', '', '', ''],
    ['', '', '', '', '', '', '', '', ''],
    ['', '', '', '', '', '.', '.', '.', '.'],
    ['', '', '', '', '', '.', '#', '#', '#'],
    ['', '', '', '', '', '.', '#', '#', '#'],
    ['', '', '', '', '', '.', '#', '#', '#']
]
for _ in range(n) :
    s = input()
    l.append(list(s))
    
for x in range(n - 9 + 1) :
    for y in range(m - 9 + 1) :
        conds = []
        for i in range(9) :
            for j in range(9) :
                s, t = l[x + i][y + j], tak[i][j]
                if t != '' :
                    conds.append(s == t)
        if all(conds) :
            print(x + 1, y + 1)